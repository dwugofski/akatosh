			<h2><a class="anchor" id="travel"></a>Travel</h2>
			<p>
				Journeys between distant cities can start in any major city. The party can find a caravan and hire them to travel quickly. The cost of hiring the caravan is 10 gold/day. To estimate the duration of the trip, use the rule of thumb that the journey between any two cities takes 2-3 days. From Bruma to Whiterun, for example, the estimation is about 6 days (three to Falkreath through the mountains, then 3 to Whiterun).
				</p><p>
				Once per day on the journey, you can roll <span class="die">1d10</span> to determine whether the group is attacked by bandits. The result of the roll is as shown below:
