			<p>
				Additonal factors such as especially high Charisma or Intelligence may add aditional modifiers at the DM&rsquo;s discretion. When dealing with fences, the standard "indifferent" DC is increased to 17, as the fences know the party has very limited options in whom to sell to. Prices for fences are increased/decreased (depending on which hurts the character) by a factor of 10%. The price provided for items noted with the "(fenced)" attribute are provided without this 10% price reduction (so an addition 10% is still applied when the character tries to sell to a fence).
				</p><p>
				When haggling, roll a CHA (Persuasion) check. If the character succeeds the check, for each point above the haggling DC (miniumum 1), complete the transaction with a 10% gain/loss on the price of the good (depending on whichever benefits the player). If the character fails the check, increase the DC by 1. This haggling DC modification applies to all party members the merchant associates with the character. Should the DC modifier cross the thresholds listed above, base price modification will be incurred.
			</p>