		<h2><a class="anchor" id="haggling"></a>Haggling</h2>
			<p>
				In some circumstances a player may wish to haggle on the price of goods. To facilitate this, salespersons will have a haggling DC. The standard "indiffient" haggling DC is 15. Certain factors, such as trying to haggle with an antagonistic/friendly salesperson, will affect this DC. Haggling relationships can also affect the base price a merchant is willing to buy/sell a good for. It is up to the DM to determine how affected a merchant&rsquo;s haggling DC and base price is affected by relationship. However a general rule of thumb is laid out in the following table. These rules of tumb are pulled from the <a href="http://www.d20pfsrd.com/skills/diplomacy/">Pathfinder diplomacy system</a>.
			</p>
			<div class="tablewrap full">
				<table>
					<tr>
						<th>Attitude</th>
						<th>Description</th>
