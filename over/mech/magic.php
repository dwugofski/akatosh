<h2><a class="anchor" id="magicka"></a>Working with Magicka</h2>
			<p>
				Unlike in the D&amp;D 5e universe, the world of Tamriel is inundated with magic. As such, the rules regarding magic had to be adjusted to make it more prevalent. This section provides an overview of the three main areas in which the characters will interact with magicka: enchanted items, scrolls, and learning spells. To see a list of magical items that I have added to the campaign, please refer to the <a href="../over/index.php?section=mag">Magical Items</a> page.
			</p>
			<h3><a class="anchor" id="enchanted_items"></a>Enchanted Items</h3>
			<p>
				Most enchanted items in the campaign will have a limited number of uses. As they are used, the magicka powering them will become drained, requiring them to be recharged. In order to recharge these uses, the party will have a few options. They can use <a href="../over/index.php?section=mag#varla_stone">Varla Stones</a> to completely restore the magicka in the object. They can use filled <a href="../over/index.php?section=mag#soul_gem">Soul Gems</a> to replenish the object. Or they can go to an enchanter who will replenish the uses of the object. Passive enchantments will not use magicka and thus will not need to be restored.
				</p><p>
