			<p>
				The maximum enchantment level which can be placed on an item is determined by the equipment&rsquo;s tier. Starter gear &mdash; tier zero &mdash; can only have petty enchantments placed on it. Tier one can have lesser enchantments. Tier two allows for common enchantments. Tier three gear can hold greater enchantments, and lastly tier four gear can bear up to grand enchantments. Once an item has been enchanted, it cannot be enchanted again.
				</p><p>
				Special enchanted items are typically gained as rewards for completing quests. Each enchanted item encountered in this way can be disenchanted to learn the spell behind it. While wizards are the only class capable of learning any spells through these means, exceptions may be made for other classes provided the spell is something related to their class. Disenchanting the item destroys it, and enchanted items with no uses cannot be disenchanted.
				</p><p>
				Player characters may enchant items themselves, but it will require the character to use a soul gem filled with a soul of the same level as the enchantment. They may then enchant the item with a spell they know, though the ultimate effect of such an enchantment will be determined by the DM. Alternately, if they allow an enchanter to use a filled soul gem, the enchanter will be willing to enchant an item with an enchantment of the same level as the filled soul for 10 gp. Or they will place an enchantment of lesser level for free. 
			</p>
			<h4>Enchanting Ammunition</h4>
			<p>
				Any enchanted ranged weapon with ammunition will take one use to charge its basic ammunition with the enchantment effect before firing. If the ammunition is itself enchanted, the enchantments will not stack and the ammunition enchantment effect will take precedence. Firing the enchanted ranged weapon in this case will not consume a use. When enchanting ammunition, the amount of amunition enchanted is equal to the number of uses rolled. You do not need to purchase base ammunition to enchant. 
			</p>
			<h4>Soul Trapping</h4>
			<p>
				In order to fill a soul gem, a character with a soul gem must cast Soul Trap on a creature and kill that creature before the spell wears off. Even then, if the character does not have a soul gem of level greater than the soul of the deceased, no souls gems will be filled. Soul Trap is a sorceror/warlock/wizard cantrip, though wizards with the Necromancy Arcane Tradition gain the cantrip automatically.
				</p><p>
				The level of a soul is determined by its challenge rating. Each level represents a tier (with &ldquo;petty&rdquo; and &ldquo;lesser&rdquo; both representing the first and second halves of the first tier respectively). If a creature would be considered a &ldquo;hard&rdquo; encounter for a single character in a given tier, that creature possesses a soul of the level corresponding to that tier. The only exception to this rule are &ldquo;black&rdquo; &mdash; NPC &mdash; souls, which can only fill black soul gems. Conversely, black soul gems can only be filled by black souls. Black souls have a soul level corresponding to grand souls, regardless of challenge rating.
				</p><p>
