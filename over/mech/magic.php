			<h4>Making Black Soul Gems</h4>
			<p>
				While other empty soul gems may only be purchased, black soul gems may be created from grand soul gems. This process is known only by necromancers. A wizard with the Necromancy Arcane Tradition will learn the ritual for creating black soul gems at 6th level. The ritual can only be performed once a day, at night, over the course of several hours in seclusion. The ritual will transform one grand soul gem into a black soul gem.
			</p>

			<h3>Learning Magic</h3>
			<p>
				The court wizards and sagacious mages throughout Tamriel are too busy with their own studies to be offering lessons to any old adventurer who wanders in off the street. If players wish to learn from those mages, they will have to provide some assitance. Typically this will involve getting their hands dirty doing whatever laborious task the mages do not want to or cannot do themselves.
				</p><p>
				Once a mage is willing to teach the party a spell, he or she will offer to educate them in an assortment of spells. The assortment will contain spells from level one spells to level five spells. For the most part these spells will belong to the wizard class. Exceptions can be made if a wizard is pursuing a niche subject and there are no good wizard spells for that area.
				</p><p>
				Only a wizard can learn spells in this manner. Additionally, if a mage chooses to charge the party member for the education, it will amount to <span class="mono bred">500 &times; caster level</span> in gp. It will also take one day per caster level of research. Wizards can only learn spells if they have spell slots available for those levels.
			</p>
			<h4>Sharing Spells</h4>
			<p>
				Wizards can learn spells from any other spellcasters. However the process of learning the spell requires 2 hours and 50 gp per spell level. Wizards cannot teach and learn simultaneously, but multiple wizards can learn from the same teacher. The number of hours per spell level is increased by a factor of 1.5 (rounded up) for each additional person (3 hr/lv for 2 learners, 5 hrs/lv for 3, 7 hrs/lv for 4, etc...). If the time to learn would be cut in half, do not count them in this calculation. If all students are savants (and there are more than one), it takes 2 hrs/lv. Gold cost is as normal for each person and additive (e.g. 2 wizards pay a total of 100 gp/lv).
				</p><p>
				The gold cost is the expensive of getting materials for practicing and recording the spells. As such, it is necessary to be in or around a town or other place spellcasting equipment can be purchased. Additonally, learning spells required a properly-equipped lab. Additonal costs can be incurred from court wizards or other mages to rent out their labs for experimenting. Typical going rates are 10 gp/hr (this cost is unaffected by the savant feature, though of course the time will be).
			</p>

