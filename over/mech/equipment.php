<h2><a class="anchor" id="equipment"></a>Equipment</h2>
			<p>
				Obviously, since the Elder Scrolls universe has its own lore-centered equipment, I needed to create an analogy for those weapons in the campaign. Traditionally, weapon improvements are fairly hard to come by in D&amp;D, so I had to figure out a good way of enhancing weapons without making them too powerful. Ultimately I settled on making equipment improvements available, but only once the characters reach a certain rank, and only through doing quests for smiths, with the exception of the first improvement, which can be bought. 
				</p><p>
				It should be noted that these equipment descriptions only include the materials the equipment is crafted out of. Since the campaign is possible to be pursued in any particular order, smiths will make their own unique brand of equipment rather than one of these armor types. The stats for these crafted items should be based on the tier of the party at the time of receiving the equipment. As well, while I have many smiths throughout Tamriel, their quests will not always yield equipment improvements if the players are not ready for them.
				</p><p>
				I figure I can either make the smith quests inaccessible if I do not want the equipement to be available to them, or I can make it so they take the equipment on credit: &ldquo;Oh I am nearing a breakthrough method of smithing. I&rsquo;ll tell you what. If you do this for me, I will give you some gold now, but once I finish crafting you a set of this armor, I will send it to you. How does that sound?&rdquo;
			</p>

			<h3>Armor</h3>
			<p>
				Armor in the Elder Scrolls universe is typically divided into different tiers based on material. Heavy armor can be made of <i>iron</i>, <i>steel</i>, <i>Dwarven metal</i>, <i>ebony</i>, and <i>Daedric metal</i>. Light armor can be made of <i>leather</i>, <i>elven metal</i>, and <i>glass</i>. There have been no consistent classes of medium armor in the Elder Scrolls games, so I had to create my own: <i>hide</i>, <i>chain</i>, <i>scaled</i>, <i>orcish metal</i>, and <i>bone/chitin</i> (e.g. bonemold, dreugh chitin, or dragon scales). As well, since there are not enough light armor types to make one for each tier of play, I decided to create a <i>studded leather</i> level after leather and <i>mithril</i> after elven.
				</p><p>
				Rather than determining which armors were or were not noisy, I decided to make it so that all heavy armors created disadvantage on stealth checks, while all medium and light armors did not.
