			<h3>Awarding Points</h3>
			<p>
				When I created this system, I figured I would reward one XP unit per quest, but I realized that was too generous. As well, it did not give me much flexibility to reward more XP for harder quests. So I ended up handing out rewards in units of eighths. So it may actually be much more practical to multiply these numbers by eight (possibly even ten) so you do not have to deal with fractions. By the time I had formalized this system, I had already started giving them fractional points, so I will stick with that. The general reward scheme is one-eighth XP for stupidly simple quests (fetch, bounties, etc.), one-fourth XP for quick-and-easy but still meaningful quests, one-half for important and somewhat challenging quests, one for difficult quests.
			</p>