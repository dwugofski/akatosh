<h2><a class="anchor" id="leveling"></a>Leveling</h2>
			<p>
				Leveling in D&amp;D 5e is based on a &lsdquo;quadratic-in-cumulative&rsdquo; curve. Meaning, if you were to draw out the experience required to reach a certain level, it would appear roughly quadratic. However the game creators did create some variance in how leveling works within each tier. In the first tier, leveling is fairly easy until you try to get to levels four and five; then it is very difficult. Once you get to level five, you enter a new tier, and advancements is basically steady, with the amount of XP needed to advance increasing linearly (quadratic-in-cumulative behavior). Then you reach level eleven and the difficulty to reach the next level goes down a bit. Then it continues to increase linearly.
				</p><p>
				Personally, I have two issues with this pattern. First, I do not like dealing with hundreds and thousands of points. In my experience, people tend to just go &plusmn;50%, so if you&rsquo;re dealing in the thousands, you just go &plusmn;500. In the tens you go &plusmn;5, etc. So it made more sense to me to just deal in normalized &ldquo;XP Units&rdquo; rather than figuring out how to deal out hundreds of points.
				</p><p>
				I also prefer this route because I reward XP on a quest-basis, encapsulating all the difficulty of however many enemies the players had to kill into the difficulty of the quest, along with any challenges or tasks the party had to deal with. I do this because I prefer the reward for the quest (XP) not require players to kill things. If the players figure out some clever way of avoiding a pit full of monsters and still complete their task, I want to reward them, not punish them. And when you give XP from quests rather than monsters, it makes more sense to group the XP into normalized units rather than hundreds of points.
			</p>
