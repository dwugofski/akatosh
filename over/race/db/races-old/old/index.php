		<h1>Racial Details</h1>
		<chapter>
			The main playable races of this campaign are the main playable races of the Elder Scrolls games: Altmer, Argonians, Bosmer, Bretons, Dunmer, Imperials, Khajiit, Nords, Orsimer, and Redguard. Statistics for each race have been chosen to reflect the abilities of the races in the games. While they would likely not be balanced with the races from vanilla 5e, racial statistics are intended to be balanced among the different races in the campaign. 

			<h2>Altmer (High Elves)</h2>
			<div class="imagepair imgleft">
				<div class="imgwrap">
					<imagebox><img src="../res/races/altmer.jpg"/><footnote>An Altmer Male</footnote></imagebox>
				</div>
				<div class="textwrap">
					<p>
						The Altmer consider themselves to be the most civilized of all races. This yellow/golden skinned race of elves come from the <em>Summerset Isles</em> and are very proficient with magicka. While many races resent their arrogant attitudes, Altmer tend to be at the forefront of arts, crafts, laws, and the sciences.
					</p>
					<div class="tcol">
						<div>
							<h3 class="bred">Attribute Bonus</h3>
							<p class="uind"><b>INT</b>: +2</p>
