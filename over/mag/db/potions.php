					<h4>Potions of Healing</h4>
					<p>
						Potions of healing restore hitpoints to characters. They break into five levels: potions of <em>minor</em>, <em>lesser</em>, <em>plentiful</em>, <em>vigorous</em>, and <em>ultimate</em> healing. The accessibility of each potion depends on the level of the party. The first four potion types can only be accessed when the players are of the corresponding tier or higher. Potions of a lower tier can also be accessed, but are by that point fairly common and will not be listed as "leveled" loot, so should be added to loot pool randomly (read: at DM&rsquo;s whim). 
						</p><p>
						Potions of ultimate healing cannot be purchased or acquired as part of loot. They can only be acquired as part of special loot rewards.
					</p>
