					<p class="uind">
						Varla Stones are relics of the Ayleid civilization which used to populate Cyrodiil. They are shards of meteoric glass believed to originate from Aetherius. By touching an enchanted item with a Varla Stone, all the magicka inside the Varla Stone will be transferred into the enchanted weapon. This will completely recharge the weapon. However this process will cause the Varla Stone to crumble, becoming unusable. It is believed that the Ayleids used to know how to keep Varla Stones intact during this process and use them multiple times, but that information has long since been lost. 
						</p><p>
						Varla Stones cannot be bought in stores, as ownership of Ayleid relics is illegal by the Alessian Order. For the same reason, no vendors in Nibenay or any region closely tied to the Empire will buy the Varla Stones. However to special collectors or magical vendors outside the Empire, Varla Stones can be sold for 100 gp. 
