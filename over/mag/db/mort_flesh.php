					<p class="uind">
						Mort Flesh is a collection of rotting flesh from a zombie or other rotting fleshy undead. It is an ingredient used in some necromantic rituals. As well, if a spellcaster wishes to cast a necromantic spell, the spellcaster may consume a number of pieces of Mort Flesh instead of using a spell slot of the same level as the pieces eaten, causing the flesh to dissolve into the air as its essence is used to fuel the spell. The process of extracting Mort Flesh from the body of a fleshy undead requires the character to make a DC 10 WIS (Medicine) check or damage the flesh, rendering it useless.
						</p><p>
						Mort Flesh is neither stocked nor purchased by most vendors, as necromancy is considered taboo in most cultures and gross in others. However practicing necromancers may be willing to purchase the Mort Flesh for 40 gp, and may have some to sell for 75 gp. 
