					<p class="uind">
						Soul gems are special crystals with the capacity to contain the energy of a person's soul. Originally created by Molag Bal, their sinister origins have had little effect in dampening their popularity. Enchanters love soul gems, as &mdash; once filled &mdash; they make enchanting exceptionally easier. Adventurers like them as well, as they provide a convenient means of recharging the spent energy of an enchanted weapon without requiring a trip to an enchanter. Still, as only recently methods of seeding soul gem mines have been discovered, their demand far exceeds their supply.
						</p><p>
						As the <a href="../over/index.php?section=mech#enchanted_items">Enchanted Items</a> section outlines, filled soul gems can be used to restore uses to enchanted items, as well as enchant items directly. Empty soul gems can only be filled by casting a Soul Trap spell on a creature and killing it before the effect of the spell wears off. Each soul gems has a level which represents the complexity of the soul which can be used to fill it: &ldquo;petty&rdquo; &ldquo;lesser,&rdquo; &ldquo;common,&rdquo; &ldquo;greater,&rdquo; and &ldquo;grand.&rdquo; 
						</p><p>
