					<p class="uind">
						Welkynd Stones are shards of meteoric glass which the Ayleids have fashioned through methods lost to time. They act as both light sources for Ayleid ruins and as pools of magicka. A spellcaster may extract the magicka from a Welkynd Stone to recover one spell slot of any level. This act will drain the magicka from the stone, causing it to crumble to dust. 
						</p><p>
						Welkynd Stones cannot be bought in stores, as ownership of Ayleid relics is illegal by the Alessian Order. For the same reason, no vendors in Nibenay or any region closely tied to the Empire will buy the Welkynd Stones. However to special collectors or magical vendors outside the Empire, Welkynd Stones can be sold for 75 gp. 
