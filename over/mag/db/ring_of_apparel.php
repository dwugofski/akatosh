<h2><a class="anchor" id="ring_of_apparel"></a>Ring of Apparel</h2>
			<p class="uind">
				The ring of apparel is an enchanted ring that allows the wearer to make his or her clothing appear in any desired manner. It is only an illusion, and as such removing the ring will cause the clothing to return to normal. The illusion does not require concentration. 
			</p>