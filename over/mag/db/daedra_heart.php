						Daedra Hearts are the hearts of lesser daedra extracted from their corpses after killing them. The process of extracting a Daedra Heart is difficult, requiring the person extracting the heart to make a DC 10 WIS (Medicine) check to determine whether they successfully extract the heart or damage it, making the heart useless. Warlocks may use an action to consume a Daedra Heart, regaining a spell slot at any level. They may also be used as a ritual component for rituals relating to Daedra.
						</p><p>
						Daedra Hearts are highly sought-after for their magical properties. As such, finding them in stock with magical vendors is exceptionally rare, though any vendor would be glad to purchase them. Daedra Hearts cost 150 gp to buy and can be sold for 90 gp.  
