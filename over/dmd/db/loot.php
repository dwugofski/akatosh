			<h3>Treasure Loot</h3>
			<p>
				Treasure loot is recovered from containers dispersed throughout dungones. There are three main sizes of containers, each with their own class of loot to draw from: <em>small</em>, <em>medium</em>, and <em>large</em> containers. In addition to some special items provided by the different containers, each container has the chance to provide a certain amount of gold. The specifics of gold rolling for trasure containers is laid out further in this section. 
			</p>
			<h4>Small Containers</h4>
			<p>
				Small containers (jars, urns, barrels, etc.) contain <span class="loot">small loot</span>, which is mostly a small amount of gold. When generating loot for a small container, roll for gold and roll a d10. If the result is 10 roll for a minor magical item, as described in table 1.3.
			</p>
			<h4>Medium Containers</h4>
			<p>
				Medium containers (chests, trunks, lockers, etc.) contain <span class="loot">medium loot</span>, which is a decent amount of gold and a few other items. When generating loot for a medium container, roll for gold and a minor magical item (see table 1.3). Then roll a d10. If the result is 10, include a treasure whose value is determined by a d100 + 50.
				</p><p>
				If the medium container is secret, locked, or trapped roll for a major magical item (see table 1.4) instead of a minor magical item. Additionally, include a treasure if the roll is 5 or higher. For compunded difficulty (e.g. secret and locked), add 1 minor magical item for each additional level of difficulty.
			</p>
			<h4>Large Containers</h4>
			<p>
				Large containers (essentially big chests) contain <span class="loot">large loot</span>, which is a large amount of gold and several special items. When generating loot for a large container, roll for gold, two minor magical items (see table 1.4), and a treasure whose value is determined by a d100 + 50. Then roll a d10. If the result is 10, include a major magical item as well.
				</p><p>
				If the large container is secret, locked, or trapped, roll for an additional major magical item (see table 1.4). Additionally, if the auxhilary roll is a success, include two major magical items instead of just one. For compounded difficulty (e.g. secret and locked), add 2 minor magical items for each additional level of difficulty.
			</p>
			<h4>Boss Containers</h4>
			<p>
				Boss containers are treated as large containers that are locked, trapped, and secret, with a successful bonus role, despite being none of those. They have large container gold, d100 + 50 treasure item, two minor magical items, and six major magical items.
			</p>
			<h4>Rolling for Gold</h4>
			<p>
				Each type of container has a gold dice associated with it plus a modifier equal to have the max roll. Small containers have a 1d4 + 2. Medium containers have a 1d20 + 10. Large containers have a 1d100 + 50. If the container is secret, increase the number of gold dice rolled by 1. Do the same for locked or trapped containers (for a total of 4 gold dice rolled for a secreted, locked, trapped container, plus one constant modifier).
			</p>
