<h2><a class="anchor" id="loot"></a>Loot</h2>
			<p>
				No grand adventure is complete without the sweet rewards for defeating the toughest foes. This section outlines the method by which I devise the rewards the players receive during the campaign. There are three main sources of loot: <em>encounters</em>, <em>treasure</em>, and <em>quests</em>. While there are many differences in loot calculation both between and within the sources of loot, so a detailed review of section must be provided. It should go without saying these rules are general rules of thumb. When I feel a situation requires a special touch for one reason or another, I usually modify my approach. Do not be surprised to see these rules broken frequently.
			</p>

			<h3>Encounter Loot</h3>
			<p>
				Encounter loot is discovered when the party defeats a group of enemies and searches the corpses for loot. Any weapons on the bodies are accessible to the players. Typically, however, enemies will not contain any special items, apart from special enemies from whom <a href="index.php?section=mag">magical items</a> may be recovered. The main loot of encounters is gold.
				</p><p>
				The amount of gold recovered from an encounter is a function of the CR of the hardest enemy, the number of enemies, and the tier of the encounter. The CR of the hardest enemy in the encounter determines the type of dice to use, according to table 1.1 below. After this amount of gold is calculated, multply the loot by a number based on the number of other enemy creatures in the encounter, outlined in table 1.2 below. Alternatively, if the encounter is small (approx. fewer than 5 enemies), roll for loot on each creature, using their CR&rsquo;s dice. 
