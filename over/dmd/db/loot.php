			<h3>Leveling Loot</h3>
			<p>
				As the characters advance, the gear provided to them increases in quality. In general, multiply any gold received by the tier of the party. For leveling details on recovered equipment, refer to the <a href="index.php?section=mech#equipment">equipment</a> section of the mechanics page. For recovered magical items, refer to the <a href="index.php?section=mech#magicka">magical items</a> section. For potion levels, refer to the <a href="index.php?section=mag#potions">potions</a> section of the magical items page.
			</p>

			<h3>Traps and Locks</h3>
			<p>
				Trap and lock DCs are determined by a 1d10 + mod roll. The constant modifier is determined by the difficulty of the lock. A 0 suggests a very easy mechanism. A 5 corresponds to a standard mechanism. 10 is comparable to a moderately difficult mechanism. A 15 is used for the toughest of mechanisms.
				</p><p>
				For traps, the damage of the trap is determined by a d8 roll according to table 1.5. For especially dangerous traps, increase the number of rolls as desired. The method of the damage should be decided by situation, but spike traps, poison gas traps, poison dart traps, and fire/ice/shock traps are fairly standard.
