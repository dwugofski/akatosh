				<h3><a id="main_temple_anc" class="anchor"></a>16. Main Temple</h3>
				<p>
					The most revered chamber in all of Skyrim for the ancient Dragon Cult, the main temple of Bromjunaar still hums with the sinister magic of the cult. Anyone with the slightest hint of magickal sensitivity feels a wave of necromantic and blood magic rush over them upon entering the room. 
					</p><p>
					The room has three layers. On the first floor at the far end of the room from the door, there is a bool of water from water leaking through cracks in the far wall. The second layer is slightly more elevated from the first - roughly the height of the average person. Characters can climb from the first level to the second. The third lawyer - the balcony to the north end of the room - is much higher than the second and cannot be climed easily.
					</p><p>
					At (A) the ritual to awaken Morokei is taking place. All the dragon cultists, including Kivas, are kneeling before the corpse of Morokei, strands of red energy leeching off of them, into his body, floating above an alter. (B) denotes the pool of water in the chamber. The pool is not deep, and the water only rises to slightly shin-level of the average person at it deepest (against the wall). (C)&rsquo;s denote outcropping balconies, decorated with arches that resemble dragon heads. (D) denotes the uppermost level
					</p><p>
					The door to the north is locked (DC 22).
				</p>
				<h4>Battle Tactics</h4>
				<p>
					At first the thugs will attempt to delay the party while the ritual complete. When the party approaches the ritual, the aura of magicka around Morokei will dispell, killing all the cultists in the room (including any surviving thugs) when it washes over them. Morokei will awaken, annoyed the party interrupted his meal. The door will then close behind the party and seal with a magical aura.
					</p><p>
					Morokei&rsquo;s tactics will typically revolve around avoiding player attaks and moving around the room, typically via blinking and hovering. He will open with his lich crystal attack if reasonable and wait for the second ability to clock in before casting again, using his turns for spells and his necrobolt attack. Any character wearing a slave collar will be unable to avoid the necrobolt attack, if he/she is the target of one.
					</p><p>
					Should Neradrys deem the battle too risky (e.g. should he fall to 5 hit points, if he should fall to 0 hit points and be revived - up to DM&rsquo;s discretion ) he will spend a turn concentrating on his ritual to bind Morokei. On his next turn after, he will sacrifice himself to bind Morokei to the alter indefinitely. He will thrust his hands forward, bright blue whisps resembling magefire will shoot forth, surrounding Morokei in a whirlwind, directing him back to the alter. As he sustains the blast, however, his hands start to be eroded by the flames, and eventually his entire body is funneled through the beam into the whirlwind. Once complete, an aura of magicka will surround the alter, trapping Morokei on it, asleep.
					</p><p>
					After that, the soldiers from Whiterun will open the doors and see the party. Captain Fralma will congradulate the party on their success, and (if the party did not explore room 8), tell the party they found a room back aways with detailed reports of Dragon Cult activities across Skyrim, as well as the rods to remove the slave collars (they were hoping Neradrys would know what they were). On behalf of the Jarl, the High King, and all the Nords, Fralma thanks the party for their assistance. 
					</p><p>
					If the party manages to vanquish Morokei, his remains, staff, and apparel will dissolve into a cloud of red haze and seep into the cracks of the room. Additionally, the party will receive 1 bonus XP for killing Morokei instead of having Neradrys bind him.
