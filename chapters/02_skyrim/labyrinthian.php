				<p>
					The Dragon Cultists have been preapring for the conflict with the forces of Whiterun. They have fortrified the ruins against invasion. As a result, they have rallied their forces for strategic defense. On the downside, the enemies are anticipating attack, and surprising them is harder than normal. Fortunately, the cultists have distributed supplies for their troops, and when the party defeats an encampment they have access to some refreshing supplies which should keep them going through the city. 
					</p><p>
					As you can see from the dungeon map, with each line of troops there is a chest. These chests are equipment chests. In general they have the following supplies (the health and magicka potions are specifically mentioned for each chest, while the remaining supplies can be assumed for all):
				</p>
				<ul class="bulletted">
					<li>Bandages</li>
					<li>10 ft. of hempen rope</li>
					<li>Three flasks of oil</li>
					<li>50 arrows</li>
					<li>10 days&rsquo; rations</li>
					<li>5 full waterskins</li>
					<li>Up to 4 leveled health potions</li>
					<li>Up to 2 leveled magicka potions</li>
				</ul>
				<h4>Abatis</h4>
				<p>
					Many of the cultists have entrenched themselves using abatis (spikey barriers). Moving across an abatis (the hard way) will require a creature to expend half their movement to leap over the barrier. The leap itself will require the creature to make a DC 15 <span class="die">DEX</span> (Acrobatics) check. On a successful check, the creature makes it over the abatis and can continue moving. On a failed check, if the roll result was greater than 7, the creature can continue with their turn. If the result of the roll is 7 or lower, the creature ends its turn and any creature which attacks it has advantage on the attack rull until the start of its next turn.
					</p><p>
					Crossing an abatis the easy way only costs two movement spaces. Any melee attacks made across the barriers (the hard way) has disadvantage on the attack roll.
				</p>

				<h3><a id="lab_entrance_anc" class="anchor"></a>1. Entrance</h3>
				<p>
					The secret entrance the party uses to enter leads to a hidden passage which opens into the main entrance of the city of Bromjunaar (Labyrinthian). Here, two Dragon Cult spellcasters cast a warding spell on the main entrance to keep the army of Whiterun out. 
				</p>
				<h4>Battle Tactics</h4>
				<p>
					Initially, the fanatics will focus entirely on maintaining concentration on their spells of warding, letting the draugr and thugs fight the party. However, as the party starts whittling down the thugs, the fanatics will turn on the adventurers and join the fray. The two thugs in the back behind the barricade will focus on using ranged attacks agains the party.
				</p>
				<h4>Loot</h4>
				<p>
					From this encounter, the party will be able to salvage 36 gold pieces. Additionally, the supply chest will have <span class="loot">2 potions of minor healing</span> and <span class="loot">2 potions of minor magicka</span>. The left and right burial urns near the north door of the room have 5 and 2 gold pieces respectively.
				</p>

				<h3><a id="lab_customs_anc" class="anchor"></a>2. Customs</h3>
				<p>
					This room once served to manage intake into the city: both peoples and goods. Shelves carved into the walls of the room served to store records of arrivals to the city. Rusted chains are dangling from the walls which once served to keep smugglers held while they awaited escorts to Bromjunaar&rsquo;s jail. 
					</p><p>
					The shelves around the room contain mostly decayed and illgible books. However one book on the shelf does appear intact. It is the journal of a cultist in charge of guarding the entrance. Most of the journal is a chronical of personal gripes and dramas, but some passages do stick out as relevant. There is a mention of several collars discovered among the rubble when the cultists first started clearing the room for occupancy. The cultist remembered them giving him the creeps just looking at them, and knowing they were always in the chest nearby made him uneasy. With the collars there was a rod which one of the higher-ups took with him back to his office to investigate.
				</p>
				<h4>Battle Tactics</h4>
				<p>
					During battle, the infiltrators will try to keep the party clustered near the door while the fanatic casts spells and makes ranged attacks against the party members. If the party flees down the hall, the infiltrators will persue, but the fanatic will remain behind.
				</p>
				<h4>Loot</h4>
				<p>
					The northern chest will contain several <span class="loot">slave collars</span>, which the elven members of the party can equip to get past the entrance door. The fanatic will have 9 gp on his body. The southern chest is locked (DC 12), but stored inside is the following:
				</p>
				<ul class="bulletted">
					<li>4 potions of minor healing</li>
					<li>3 potions of minor magicka</li>
					<li>52 gp</li>
				</ul>
				<h4>The Entrance Door</h4>
				<p>
					The ancient Nords were not particularly friendly towards the elves. The eastern exit to the entrance square is enchanted to prevent elves from walking into the city unless wearing a slave collar. As the party approaches the door, Nordic runes will glow along its border, and even when the door is opened, they will remain in the air, hovering in place. <span class="npc">Neradrys</span>, or any other character who can read ancient Nord will be able to translate the runes. They read as follows:
					</p><p class="quote short">
					If of elven kin you be, in these halls you won't walk free
					</p><p>
					Any Bretons or elves &mdash; Neradrys included &mdash; will be met with an impregnible magical wall of force preventing them from walking past the door without the collar. 
					</p><p>
					Once attached, the collars cannot be removed, except with the removal rod stored in room 8. 
				</p>

				<h3><a id="lab_court_anc" class="anchor"></a>3. Main Court</h3>
				<p>
					Upon gaining entry to the city, new arrivals would be greeted with the enormous main court. Originally, reliefs in the walls and giant columns depicted the history and formation of the city of Bromjunaar. However age has worn down the reliefs, and the reliefs can no longer be made out. This court used to be used as the main trading center of the city.
				</p>
				<h4>Battle Tactics</h4>
				<p>
					This encounter is divided into two sections. The front line will attack the party as soon as they walk through the entrance. However, only the draugr will make melee attacks. The thugs and the infiltrators will focus on staying behind the barriers and making ranged attacks. If the party breaks past the draugr, the four thugs on the flanks of the abatis will switch to melee attacks and move forward. If the party breaks through those thugs, the four remaining thugs will attack with melee. The infiltrators will only use melee attacks as a last resort. They will try to use the protection of the mbrriers as much as possible.
					</p><p>
					The second encounter involves the enemies to the right of the red dotted line. The party can see these enemies when they enter the room. However these enemies will not attack until a member of the party crosses the red dotted line. The tactics are the same as the first group, except that the second group of thugs will remain behind the barriers as long as possible.
				</p>
				<b4>Loot</b4>
				<p>
					The gold distribution among the enemies is as follows: 6 gp for the draugr, 30 gp for the thugs, and 6 gp for the infiltrators, for a total of 42 gp from enemies. There are six main sources of treasure: two equipment chests, three urns, and a secret chest. The top. bottom-right, and bottom-left urns contain <span class="loot">3, 4, and 4 gp</span> respectively. The left equipment chest contains <span class="loot">4 potions of minor healing and 2 potions of minor magicka</span>. The right equipment chest contains <span class="loot">3 potions of minor healing and 1 potion of minor magicka</span>.
					</p><p>
					The secret chest in the top-left corner of the room requires of DC 18 <span class="die wis">WIS</span> (Perception) check to detect. The chest has a DC 8 lock. It contains:
				</p>
				<ul class="bulletted">
					<li>3 potions of minor healing</li>
					<li>1 potion of minor magicka</li>
					<li>87 gp</li>
					<li>A rusted engraved ancient Nordic tower shield (84 gp)</li>
				</ul>
				<h4>Hidden Room</h4>
				<p>
					In the north-east corner of the room there is a passage to another room. It is hidden by rubble which has fallen and appears to block passage into the room. However a DC 16 <span class="die wis">WIS</span> (Perception) check can determine that the rocks and be moved and the passage cleared.
					</p><p>
					The room contains two chests. The first chest is on the right side of the wall and is locked DC 13. It contains
				</p>
				<ul class="bulletted">
					<li>2 potion of minor healing</li>
					<li>2 potion of minor magicka</li>
					<li>88 gp</li>
				</ul>
				<p>
					To the south, under some light rubble is another hidden chest. A DC 10 <span class="die wis">WIS</span> (Perception) check will detect the chest. Another DC 14 <span class="die wis">WIS</span> (Perception) check will determine the chest is trapped. The trap is a DC 15 gas trap which inflicts 1d4 poison damage to anyone within 10 feet. It contains the following loot:
				</p>
				<ul class="bulletted">
					<li>5 potion of minor healing</li>
					<li>2 potion of minor magicka</li>
					<li>42 gp</li>
					<li>Four mummified cats, mummified according to ancient Nordic burial practices (10 gp each)</li>
					<li>Spell of Unseen Servant</li>
				</ul>
				
