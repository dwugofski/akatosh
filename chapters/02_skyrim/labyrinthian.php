				<h1><a id="labyrinthian_anc" class="anchor"></a>Labyrinthian</h1>
				<h2>Quest Overview</h2>
				<h5>XP: 1 1/2</h5>
				<p>
					With <span class="npc">Neradrys Telvanni</span> in tow, the party returns to Whiterun to discuss further actions with the Jarl. In a meeting with the Jarl, Neradrys tells the group that <span class="npc">Kinvas</span> is the leader of the recent Dragon Cult revival. Not only is Kanvas using a very powerful relic from Labyrinthian to keep the Jarl&rsquo;s troops out of ruined city, but Kinvas plans to resurrect <span class="npc">Morokei</span>, a Dragon Priest who ruled in Bromjunaar before its fall. 
					</p><p>
					Neradrys urges the Jarl to send a contingent of soldiers to Labyrinthian immediately; if Morokei is allowed to rise, he could raise an army of undead which could take over Skyrim. Although weakened, Neradrys believes he can open a tear in the blizzard long enough to send soldiers through to fight the Dragon Cult. Impressed with the party&rsquo;s seige on the Dragon Cult lair in Volunruud, he asks the party to go with the contingent.
					</p><p>
					The party, along with Neradrys and <span class="npc">Captain Fralma</span>, lay seige to Labyrinthian. While the main forces distract the Dragon Cult troops, Frelma and Neradrys suggest the party seek an alternate route into the main city, as there are too many Dragon Cultists to make a direct assault quickly. With the help of a Nordic ghost, the party finds an alternate entrance. After a sequence of trials, the group is able to enter the main city and fight their way to Kinvas and Morokei, who has indeed been resurrected.
					</p><p>
					Once Kinvas is dead and Morokei is bound, the party can collect the <span class="item">Bal Falruhn</span>.
				</p>

				<h2><a class="anchor" id="return_to_the_jarl_anc"></a>Return to the Jarl</h2>
				<p>
					After the party rescues <span class="npc">Neradrys Telvanni</span>, they can return to <span class="npc">Jarl Hormund Banner-Shield</span>, who will, considering Neradrys a co-conspirator to the Dragon Cult, initially treat Neradrys with suspicion and hostility. Neradrys explains the details he was able to learn during his captivity with the Dragon Cult. 
					</p><p>
					He explains the leader of the revival is a man named <span class="npc">Kinvas</span>. The Dragon Cult revival had apparently been going on underground for quite some time. It was not until Kinvas, who lead a small faction in Labyrinthian, found a powerful relic left there that he was able to convince the cultists to come out of hiding. With the relic, he created the blizzard surrounding Labyrinthian. He kept Neradrys in order to learn how to master the power of the relic.
					</p><p>
					Now that he believes he has acquired all the knowledge he can, Kinvas is shifting his focus back on the main goal of reviving the Dragon Cult. Unfortunately, Kinvas plans to do so by reviving one of the High Dragon Priests, <span class="npc">Morokei</span>. Kinvas believes that harnessing the power of the relic, he can resurrect Morokei. If he does so, all the undead Draugr of Labyrinthian &mdash; and possibly across all of Skyrim &mdash; will rally around Morokei as a leader and attempt to conquer Skyrim and re-establish the Dragon Cult. 
					</p><p>
					Neradrys believes his rescue will accelerate Kinvas&rsquo;s plans, as he no doubt figures that Neradrys knows how to get troops pass the blizzard. Fearing an attack, Kinvas will almost certainly begin the process of reviving Morokei. Neradrys states that he can get a group of soldiers past the blizzard, and that the Jarl should launch a seige immediately, while they still have time. 
					</p><p>
					The Jarl agrees with Neradrys, but states that he cannot rally an entire force, so it will have to be a small contingent. In light of their successin Volunruud, the Jarl asks the party if they would be willing to accompany the troops.
				</p>

				<h2><a class="anchor" id="the_labyrinthian_seige_anc"></a>The Labyrinthian Seige</h2>
				<p>
					With a contingent of fifty soldiers, the party, Neradrys, and <span class="npc">Captain Fralma</span> &mdash; the leader of the troops &mdash; move out to Labyrinthian. When they arrive, Neradrys explains that the blizzard is actually deceptively thin. It is like a sheet of cloth; ultimately thin, but as you push against it, it gets harder to push farther. All he has to do is puncture a small tear in the veil, and keep it open long enough for the party to get through. 
					</p><p>
					Once through, the troops engage the cultists outside Labyrinthian in combat. Neradrys becomes worried about how long a frontal assault will take, but Frelma states they have no other options without another way into the main city. Neradrys suggests that &mdash; for a city as old and important as Bromjunaar &mdash; there are bound to be special entrances used in case of emergencies. 
					</p><p>
					Neradrys suggests summoning a ghost to ask about an entrance. The summoned ghost states that he does know a hidden entrance to the city, but he will only tell the party if they prove worthy of the knowledge. The ancient Nords proved worth through combat, so the party must fight the ghost. Alternatively, the party may prove their worth by boasting of their success, or otherwise convincing the ghost of their worth as warriors without actually battling the ghost. 
					</p><p>
					When the ghost is convinced (the battle does not have to go until the ghost is dead), he will escort the party to the entrance of a cave that is hidden. Before they leave, Frelma offers them each 5 health potions. Neradrys tags along, explaining that he is too drained from ripping through the veil and summoning the ghost to be of much assistance in combat, but he knows the language of the ancient Nords, so he could be of help if they need any translations.
				</p>

