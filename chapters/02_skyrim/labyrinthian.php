				<p>
					The ghost informs the party that this cave will lead them to the trials of Bromjunaar &mdash; four trials which only one with the spirit of a true Atmoran would be able to complete. These puzzles were crafted to keep enemies of Bromjunaary out, but were common knowledge to the elites of the region. 
				</p>
