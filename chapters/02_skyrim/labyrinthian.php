				<h3><a id="bar_room_anc" class="anchor"></a>5. Cult Tavern</h3>
				<p>
					Casks of ale are lined up along the bottom wall, to the left, along with some cooking prep surfaces, some mugs, and a fire. It appears this room was being used as a tavern for the cultists before the seige.
				</p>
				<h4>Battle Tactics</h4>
				<p>
					The two cultists flanking the barriers will attempt to block the party in while the cultist opposite the barrier as well as the wights and fanatic attack at range. If the party manages to break past the barrier, the wights and the remaining thug will switch to melee attacks. 
				</p>
				<h4>Loot</h4>
				<p>
					The intact urn in the room contains <span class="loot">1 gp</span>. The encounter provides <span class="loot">11 gp</span>.
				</p>
				
				<h3><a id="stair_room_anc" class="anchor"></a>6. Stair Room</h3>
				<p>
					It is clear this room served more as a courtyard of sorts. However any decorations and beauty has long since faded. The cultists have their cots laid out and a fire going in the corner; this is one of their major camps.
				</p>
				<h4>Battle Tactics</h4>
				<p>
					As with previous rooms, basic flanking strategy. The two thugs block passage. Everyone does ranged attacks. The wights and forward thugs move in if the party makes it past the first two thugs. The four end thugs break off and go around. Then the two remaining. The infiltrator does range as long as possible.
				</p>
				<h4>Loot</h4>
				<p>
					The encounter provides <span class="loot">16 gp</span>. The equipment chest contains <span class="loot">1 potion of minor healing and 1 potion of minor magicka</span>. The chest in the back of the room is locked DC 6 and contains:
				</p>
				<ul class="bulletted">
					<li>17 gp</li>
					<li>2 potions of lesser healing</li>
				</ul>
				
				<h3><a id="torture_room_anc" class="anchor"></a>7. Torture Room</h3>
				<p>
					Rusted iron cages dangle from the ceiling. Horrible torture devices line the walls. It is clear this is where the ancient Nords tortured their victims. However from the looks of the devices, that practice has been resumed.
				</p>
				<h4>Battle Tactics</h4>
				<p>
					Basic flanking pattern. Wights are fodder. Nearest thugs break from barriers. 
				</p>
				<h4>Loot</h4>
				<p>
					The encounter provides <span class="loot">30 gp</span>. The equipment chest contains <span class="loot">1 potion of minor healing and 1 potion of minor magicka</span>.
				</p>
				
				<h3><a id="lieutenants_office_anc" class="anchor"></a>8. Lieutenant&rsquo;s Office</h3>
				<p>
					This is the office of one of Kivas&rsquo;s lietenants. On the top side of the room are a couple tables with maps and charts on them detailing locations of dragon cult cells across Skyrim. The shelves on the left side of the room contain records of different financial dealings the cultists use to fund themselves. As the party enters, the lieutenant is discussing battle strategies for Morokei&rsquo;s army once the ritual is complete.
				</p>
				<h4>Battle Tactics</h4>
				<p>
					The wights try to keep the party near the door. The fanatics try to get to a safe distance and attack with spells. The champion goes in for melee.
				</p>
