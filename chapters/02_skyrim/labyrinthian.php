				<h3><a id="wolf_room_anc" class="anchor"></a>1: The Wolf Room</h3>
				<p>
					The first room has two rows of seven buttons on each side of the room. On each button is the symbol of an animal. Going from top-right and clockwise, the images on the buttons are fox, fox, rabbit, snake, rabbit, snake, snake, fox, rabbit, snake, fox, rabbit, fox, snake (just random sequence of snake, rabbit, and fox). On the far end of the room is a protcullis in front of a door. Above the protcullis is the symbol of a wolf. Near the entrance of the room is a pedastle on which the following inscription is made in Atmoran:
					</p><p class="quote">
					The wolf hunts in packs, working together to eliminate its prety. So too are the Nords united; they walk the same path.
					</p><p>
					The solution to the puzzle requires the group to press a button &mdash; any button &mdash; together. When they do, the protcullis opens and they can continue on.
				</p>
				<h3><a id="bear_room_anc" class="anchor"></a>2: The Bear Room</h3>
				<p>
					The second room has a hallway in the center where the floos is covered in pressure panels that, when compressed, will cause spikes to shoot up. Lining the walls of the hallway are six buttons. On the far end of the hallway on the wall is the emblem of a bear. To the right (when facing) of the bear is a portcullin in front of a door. Near the entrance is a pedastle with the following inscription in Atmoran:
					</p><p class="quote">
					The bear is strong and fearless; it charges forward without fear of harm. Likewise the Nord does not shirk from pain, and marches assuredly into battle.
					</p><p>
					The solution to the puzzle requires a member of the group to step on a pressure plate without trying to avoid taking damage. When he or she does so, all the plates will compress without activating, the portcullis will open, and the party may continue.
				</p>
				<h3><a id="hawk_room_anc" class="anchor"></a>2: The Hawk Room</h3>
				<p>
					The third room has three platforms forming a fork shape with a chasm between them. At the far end of each platform is a button. From top to bottom they are: rabbit, snake, fish. On the far end of the room, across a chasm, is another platform, with a procullis and a door. Above the door, level with the three platforms is the emblem of a hawk. The party may simply jump down onto the lower platoform, but cannot jump back up. Near the entrance of the room is a pedastle with the inscription: 
					</p><p class="quote">
					The hawk is keen and observent. Its eye always finds its target. Likewise, a true Nord is not mislead by lies, and always knows his true enemy.
					</p><p>
					The solution to the puzzle is to throw some projectile &mdash; a rock or some such &mdash; at the eye of the hawk. The party may detect that the eye is special by making a DC 18 INT (Investigation) check. 
				</p>
				<h3><a id="owl_room_anc" class="anchor"></a>2: The Owl Room</h3>
				<p>
					The fourth room has four buttons in the four corners of the room. There is a portcullis and a door behind that. In the center of the room is a pedastle with an inscription which reads
					</p><p class="quote">
					The owl is wise and strategic. It does not persue a path thoughtlessly. Neither does a Nord go into battle without being certain of its necessity.
					</p><p>
					The solution to the puzzle is cruel in its simplicity. The portcullis can be lifted; it is not weighed down.
				</p>

				<h2><a class="anchor" id="labyrinthian_proper_anc"></a>Ruins of Labyrinthian</h2>
