				<h1><a id="the_brains_of_the_operation_anc" class="anchor"></a>The Brains of the Operation</h1>
				<h2>Quest Overview</h2>
				<h5>XP: 1/4</h5>
				<p>
					<span class="npc"><a href="#Daeniella_Lavina">Daeniella Lavina</a></span> is looking for assistance with her research into mind control magics. She needs a human brain, but <span class="npc"><a href="#Priest_Vronjar">Vronjar</a></span> refuses to let her take the brains of the deceased in the <span class="loc"><a href="#section 1_2_5">Hall of the Dead</a></span>. If the party would could get her a human brain, she would be willing to return the favor with a powerful scroll or a lesson on mind altering magics. 
					</p><p>
					This quest is intentionally open-ended. It is up to the players to determine how to acquire the brains. This is a prime improvisational quest.
				</p>
				
				<h3><a id="talking_with_daeniella_anc" class="anchor"></a>Talking with Daeniella</h3>
				<p>
					Daeniella is obsessed with her work, and sees herself as the foremost expert on mental manipulation through magic. She works tirelessly to advance the field. She has very little time to engage in frivolous chatter. If the party expresses a willingness to aid in her research, she will explain that her research has been slowed by her inability to experiment on real human brains. She would hire some mercernaries to go out, hunt down some bandits, and take their brains, but in her experience when the person dies a violent death their brains don&rsquo;t have the same effectiveness. 
					</p><p>
					She has tried to get some from the Hall of the Dead, but Vronjar considers it to be sacrilige and desecration, so she is no longer allowed in the catacombs anymore. Used to be she could just charm him to let her take what she needed, but recently her charm spells have not been holding over him. If the party could get into the catacombs and &ldquo;acquire&rdquo; some human brains, her research efforts would be greatly sped up, and she would be able to help the party.
				</p>
				
				<h3><a id="harvesting_abain_norman_anc" class="anchor"></a>Harvesting Abain Norman</h3>
				<p>
					The Hall of the Dead is composed of two main areas: the central hall is where the deceased are prepared for burial, and where the family visits during funerals. The catacombs below is where the actual deceased are buried, either their bodies or their ashes. At present there is only one body being prepared in the central hall: a Breton by the name of <span class="npc">Abain Norman</span>. 
					</p><p>
					Naturally, Vronjar spends most of his time either performing last rites over Abain or purifying the catacombs to keep the dead from rising. He is not a particular lively or energetic man, and his focus often drifts during conversations. However if he does sense the party is trying to desecrate the remains of Abain or any other corpse in the Hall of the Dead, he will cast them out and refuse to let them back while they still live.
				</p>
				
				<h3><a id="the_brains_of_the_operation_reward_anc" class="anchor"></a>Reward</h3>
				<p>
					As a reward, Daeniella will either give the party one of the following scrolls, or &mdash; in the case there is/are wizard(s) in the party &mdash; train party members in one of the following spells:
				</p>
				<ul>
					<li>Charm Person</li>
					<li>Crown of Madness</li>
					<li>Hypnotic Pattern</li>
					<li>Confusion</li>
					<li>Modify Memory</li>
				</ul>
