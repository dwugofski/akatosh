				<h2><a id="kemen_anc" class="anchor"></a>Kemen</h2>
				<imagebox id="kemen_map_box" class="doublewide noprinting">
					<img src="res/kemen.png" id="kemen_map" alt="Kemen Map"/>
					<script type="text/javascript">addNewImageViewer('kemen_map');</script>
					<footnote>Figure 1.1: Map of Kemen</footnote>
				</imagebox>
				<p>
					Kemen is located to the east of Cheydinhal, at the top of a large hill. Given its proximity to the city, the selectives assumed no one would bother them for fear of getting discovered by the watch entering an Ayleid ruin. Unfortunately for the Selectives, a group of Mehrunes Dagon cultists though similarly and had already established a shrine in the ruins. When the Selectives met the Dagon cultists, the cultists ambushed the Selectives. One by one the Selectives were tortured, mutilated, and killed as sacrifices to Dagon. 
					</p><p>
					By the time the party enters Kemen, all but one of the Selectives have been killed. Evidence of a recent battle is apparent, with blood splatters on the walls and floors. Many of the cultists are still washing blood off their clothes. The party must fight their way through the cultists to reach the last survivor to get access to the notes.
					</p><p>
					Stats for the Dagon cultists match that of the (coincidentally named) Dragon cultists. Most of these cultists will be unsuspecting &mdash; either sleeping, eating, or carrying out daily chores. However it is possible for especially loud sounds to alert nearby cultists of the intruders. In such circumstances, the other cultists may warn others, confront the intruders, or hold back and prepare for battle.
				</p>

				<h3><a id="kemen_entrance_anc" class="anchor"></a>1. Entrance</h3>
				<p>
					The entrance to Kemen resembles that of any typical Ayleid ruin. However, unlike most Ayleid ruins, the floor is littered with blood pools and streaks. The blood is pretty fresh - no more than a couple days old. 
				</p>

				<h3><a id="kemen_stockpile_anc" class="anchor"></a>2. Stockpile</h3>
				<p>
					This room is used by the Dagon Cultists to store the gold and special loot recovered from their raids. Sadly, they have not had much luck pulling off raids this close to Cheydinhal. 
				</p>
				<h4>Combat</h4>
				<p>
					In this room are two Dagon Infiltrators. By default, they will be recording the loot obtained from the Selectives in a record. The Infiltrators will attempt to bottleneck the party at the doorway if possible.
				</p>
				<h4>Loot</h4>
				<p>
					Loot from the encounter will total to <span class="loot">38 gp</span>. The chest in this room has a DC 7 lock (openable with the Cult Leader&rsquo;s Key) and contains <span class="loot">58 gp, a scroll of Daylight, a welkynd stone,</span> and assorted stolen trade goods worth about <span class="loot">92 gp</span> (will need a fence to sell).
				</p>

				<h3><a id="kemen_grand_hall_anc" class="anchor"></a>3. Grand Hall</h3>
				<p>
					This room resembles a grand hallway, with an exceptionally tall ceiling and wide berth. Currently the cultists seem to be using it as a barracks.
					</p><p>
					In the center of the room is a rising floor trap. If a creature steps in the highlighted 15 ft by 15 ft square, the character must make a DC 11 DEX saving throw or take 1d4 piercing damage. Additionally, the creature exhausts its move action if it activates the trap. A creature which starts its turn does not activate the trap until its turn ends. All creatures in the highligted area are affected by the trap simultaneously.
					</p><p>
					The northern door contains a DC 10 lock and trap which deals 1d4 fire damage.
				</p>
				<h4>Combat</h4>
				<p>
					Having spent much time in these ruins, the cultists know about the spike trap. As a result, those behind the trap will resort to ranged attacks while those on the other side will continue to use melee attacks.
				</p>
				<h4>Loot</h4>
				<p>
					The encounter in this room will yield no gold. However the left and right urns contain <span class="loot">6 and 5 gp</span> respectively. There are <span class="loot">3 welkynd stones</span> in the room, though no more than 2 are on the same side. There is a <span class="loot">varla stone</span> south of the rising spike trap. To the north of the rising spike trap is a DC 15 (to detect and/or unlock) ayleid chest containing <span class="loot">74 gp, an Ayleid brooch worth 85 gp (must be fenced), a black soul gem, and 7 lesser shock arrows</span>. The locked ayleid chest to the south-west of the room contains <span class="loot">13 gp and 2 potions of lesser healing</span>.
				</p>

				<h3><a id="kemen_lecture_hall_anc" class="anchor"></a>4. Lecture Hall</h3>
				<p>
					This room is lined with seats, suggesting the room was meant to serve some form of discussion or lecture hall.
					</p><p>
					The hallway after this room is a DC 10 spike trap dealing 1d8 piercing damage. Each of the doors has a DC 13 1d4 acid splash trap on it.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">19 gp</span>. The left and right urns contain <span class="loot">4 gp</span> each.
				</p>
				<h4>Hidden Room 4.1</h4>
				<p>
					With a DC 11 perception check, the party can detect a small ayleid button in the northern wall which will lower a wall leading to a hidden room. The hidden room has a large chest with DC 15 lock containing <span class="loot">240 gp, a lesser soul gem, 4 lesser fire arrows, a gilded Ayleid cowel worth 145 gp (must be fenced), and a potion of ultimate healing</span>.
				</p>

				<h3><a id="kemen_zigzag_anc" class="anchor"></a>5. Zig-zag Room</h3>
				<p>
					This is one of the Ayleid catecomb rooms, where they laid their dead to rest. Three cultists are chatting among themselves as they dump the body of a Selective they killed. 
					</p><p>
					The portcullis (actually a square metal grating with an intricate vine design and a whole in the center) to this room requires activation of a switch on the right side of the door.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">7 gp</span>. The bottom and top urns are hidden behind sarcophagi and reuire a DC 12 and 14 detection check respectively to be noticed. They contain <span class="loot">7 and 9 gp</span> respectively. The northern check requires a DC 9 check for detection and has a DC 11 1d4 shock trap on it. It contains <span class="loot">62 gp, a potion of ultimate healing, and 2 potions of minor magicka</span>.
				</p>

				<h3><a id="kemen_cafeteria_anc" class="anchor"></a>6. Cafeteria</h3>
				<p>
					The 15-foot wall dividing this room does not reach all the way to the ceiling, but it is capable of blocking line of sight to the other half of the room. By default, the cultists will be occupied eating.
					</p><p>
					The door leading out has a DC 15 lock.
				</p>
				<h4>Combat</h4>
				<p>
					Should the right side be attacked before the left, the two thugs will come running to the other side to get the help of their friends. Should the party be detected by those on the left first, they will shout for the two on the right.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">10 gp</span>. The Ayleid chest in this room contain <span class="loot">27 gp and a scroll of Slow</span>.
				</p>

				<h3><a id="kemen_library_anc" class="anchor"></a>7. Library</h3>
				<p>
					While not a library per se, in this room the cultists just appear to be sitting and staring into nothing. As if they are in a trance. In fact, unless otherwise alerted, sneaking past these cultists only requires a stealth DC of 3.
					</p><p>
					The locked door to the north has DC 14.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">11 gp</span>. The left and right urns in this room contain <span class="loot">3 gp</span> each. 
				</p>

				<h3><a id="kemen_dicussion_anc" class="anchor"></a>8. Discussion Room</h3>
				<p>
					This room is lined with seats, suggesting the room was meant to serve some form of discussion or lecture hall.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">52 gp</span>. The urn in this room contain <span class="loot">3 gp and 2 potions of minor healing</span>.
				</p>

				<h3><a id="kemen_pit_anc" class="anchor"></a>9. Pit Room</h3>
				<p>
					Half of this room is significantly more elevated than the other. There is a ramp of stairs leading up to the elevated half, but much of it is crumbled. A DC 17 DEX (Acrobatics) check is required to successfully make it over to the next level. On a failed check, the character takes 1d6 bludgeoning damage. Once a character is over the leap, he or she can lift others up.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">36 gp</span>. The medium chest in this room has a DC 8 1d8 shock trap placed upon it and contain <span class="loot">45 gp and 8 common ice arrows</span>. Additionally this room contains <span class="loot">2 welkynd</span>.
				</p>

				<h3><a id="kemen_guillotine_anc" class="anchor"></a>10. Guillotine Room</h3>
				<p>
					This room is a long hallway with for guillotine traps perpendicular to its length. These traps are as wide as the room and can damage any creature passing through them. Each guillotine acts as a DC 9 1d4 slashing damage trap. The guillotines are constantly dropping at a consistent rate. This DC takes awareness of the trap into account (i.e. characters do not get advantage on the DEX saving throw simply for knowing the trap is present).
				</p>
				<h4>Hidden Room&rsquo;s Loot</h4>
				<p>
					The room to the south has a hidden wall. A DC 12 detection check will indicate a false stone button in the adjacent wall which will cause rectangular pillars in the wall to descend into the floor, granting access to the treasure room. There are three large chests in this room. The top chest contains <span class="loot">62 gp, 2 potions of lesser magicka, a scroll of aid, and an ayleid necklace worth 138 gp (fenced)</span>. The middle chest is locked DC 6 and contains <span class="loot">190 gp, 5 lesser fire arrows, a scroll of Earthen Grasp, a potion of lesser healing, and an engraved empty Welkynd stone worth 92 gp</span>. The bottom chest has a DC 15 1d4 spike trap and contains <span class="loot">197 gp, two lesser soul gems, a black soul gem, and a couple Ayleid ritual tablets totaling 55 gp each (fenced)</span>.
				</p>

				<h3><a id="kemen_officers_anc" class="anchor"></a>11. Officers&rsquo; Quarters</h3>
				<p>
					This chamber appears to be where the cultist officers rest.
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">37 gp</span>. The medium chest in this room has a DC 8 1d8 shock trap placed upon it and contain <span class="loot">45 gp and 8 common ice arrows</span>.
				</p>

				<h3><a id="kemen_sacristy_anc" class="anchor"></a>12. Sacristy</h3>
				<p>
					While most of the rooms thusfar have appeared loosly decorated, this is the first room with significant ritual ornamentation. It appears this is the room where the cult leaders prepare for their rituals. To the north, one of the cultists is washing his bloodied robes in a basin. The door to the south is locked DC 19. The cult leader has a key which unlocks the door to the south as well as unlocking and disarming certain cultists&rsquo; chests in the dungeon. 
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">23 gp</span>. Two welkynd stones are placed in sconces on either side of the room to provide light. The medium chest to the north is locked and trapped with a DC 12 1d4 poison gas trap (though it can be unlocked/disarmed with the key on the leader) and contains <span class="loot">54 gp, 2 potions of lesser magicka, a lesser soul gem, an Ayleid toy horse worth 87 gp (fenced)</span>. Additionally this room contains <span class="loot">2 welkynd stones</span>.
				</p>	

				<h3><a id="kemen_shrine_anc" class="anchor"></a>13. Dagon&rsquo;s Shrine</h3>
				<p>
					As the party descends the stairs to the deepest part of Kemen, they see a bright orange glow. It appears almost as if the room below is on fire. Indeed, as they enter the room, ir appears grooves have been carved into the walls to make room for some flammable material to be placed, as fire lines the room at about chest height. Sconces for torces have been nailed into the columns around the room. An alter with the statue of Mehrunes Dagon has been erected to the south of the room, and fire lines the alter. The heat, dryness, and sheer lack of oxygen makes this room feel oppressive and unyielding. 
					</p><p>
					Around the alter are the bodies of several Imperial men, nakend and showing signs of obvious torture (long cuts, bruises, bones sticking out of the skin, missing appendeges, missing eyes, and holes drilled into various parts of the body). Before the alter to the south, a man dressed in black robes, bloodied and beaten, is shackled between two posts, clearly erected as a sacrifice to Lord Dagon. At the base of Dagon&rsquo;s shrine is a basin filled with blood and various body parts (tongues, eyeballs, fingers, and guts in general). The cultists are mumbling a chant to Dagon, with their leader clearly performing some rite over the man.
				</p>
				<h4>Defeating the Cult</h4>
				<p>
					When the cult leader dies, the flames around the room will be sucked into a vortex in front of the Dagon statue, forming a vortex of fire. Tendrils of flame will shoot out and wrap themselves around the cultists, as the party must make a DC 7 STR saving throw to avoid being knocked prone as the wind rushes from behind them into the vortex. A hand seemingly made of fire reaches forth from the vortex and grabs the sacrifice, pulling him into the vortex, screaming in agony as his hands are ripped through the shackles, his skin tearing and bones breaking in the proccess. 
				</p>
				<h4>Loot</h4>
				<p>
					This encounter yields <span class="loot">24 gp</span>. The boss chest to the north contains <span class="loot">346 gp, 1 potion of minor magicka, 12 lesser shock arrows, 1 common fire arrow, 12 common ice arrows, 12 common shock arrows, 2 potions of lesser healing, and a gilded carafe engraved with the figures of the eight divines worth 144 gp</span>.
				</p>
