				<h1><a id="kemen_sect_anc" class="anchor"></a>Investigating Kemen</h1>
				<h2>Quest Overview</h2>
				<h5>XP: 1</h5>
				<p>
					After asking around the city trying to locate the Blades agent in the city, the party will be pointed in the direction of <span class="npc">Karag Gro-Elimack</span>, whose clothing shop has been vandalized by newcomming strangers. When interviewing Karag, he will communicate to the party that a group of Marukhati Selectives raided his shop, stole his notes on the artifact he was researching, and erased his memory. However he believes the Selectives ran off to <span class="loc"><a href="#kemen_anc">Kemen</a></span>. 
					</p><p>
					The party goes to Kemen to investigate, and discovers evidence the selectives were ambushed by a group of Daedra worshippers who brutally slaughtered the Selectives, save for one. However when the party gets to the survivor, he is ripped through a small portal into the Deadlands, and the party must figure out a way to follow in order to retrieve Karag&rsquo;s notes.
				</p>

				<h3><a id="finding_karag_anc" class="anchor"></a>Finding Karag</h3>
				<p>
					A few days before the party arrived, a group of visitors ransacked the clothing shop of <span class="npc">Karag Gro-Elimack</span>. They dealt significant damage to his store, but did not rob him. As it turns out, the vandals were Marukhati Selectives. They had discovered Karag&rsquo;s identity and were sent to steal his research notes and erase his memory. While Karag remembers having studied the location of a relic, he cannot remember the details of the relic. While his notes are encoded, given enough time the Marukhatis may be able to decipher them and beat the party to it. After some recovery and investigation, Karag believes the Selectives are operating out of the nearby Ayleid ruins of Kemen.
					</p><p>
					Even after the party signals to Karag their affiliation with the Blades, the orc will not drop his cover as a clothier unless unavoidable. He will speak of the Selectives as &ldquo;competitors,&rdquo; amd his research on the relic as his &ldquo;revolutionary design sketches.&rdquo; He will insist upon being a &ldquo;humble tailor,&rdquo; but will &ldquo;wink-wink, nudge-nudge&rdquo; the party to tip them off if they actually think he&rsquo;s just a tailor.
				</p>

				<h3><a id="knowing_karag_anc" class="anchor"></a>Character Description: Karag</h3>
				<p>
					After meeting with Karag, the part will be able to travel with Karag should they choose to so. As a potential party NPC, here are some guidlines to Karag&rsquo;s character:
					</p><p>
					Karag thoroughly insists on maintaining his cover as a tailor at all times. He actually is a pretty good tailor. He&rsquo;s always impeccably dressed, and even when he becomes enraged, his clothes seem painstakingly tailored for his buff build. Karag hides behind his cover mostly to keep himself distanced from his past. As a result, he will attempt to deflect any questions about his background. &ldquo;Why does a tailor have an expertly crafted greataxe?&rdquo; &ldquo;Being a tailor in Cheydinhal is risky business. Have to defend myself somehow from all the merchant lord daughters swarming my shop.&rdquo; &ldquo;How do you know so much about X?&rdquo; &ldquo;You overhear all sorts of gossip when you&rsquo;re doing a dress fitting.&rdquo;
					</p><p>
					As any spy, Karag does not truly trust anyone but himself. However he is very quick to sense when people are lying about their motives, so he does trust that &mdash; should his goals align with someone else&rsquo;s &mdash; cooperation will be beneficial.
				</p>
