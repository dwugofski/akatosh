				<h2><a id="after_kemen_anc" class="anchor"></a>After Kemen</h2>
				<p>
					After the survivor is pulled through the vortex, Karag will go charging in, shouting, "Nooooo!" However the protal will close too soon for anyone to jump in after him. None of the bodies have the Selectives&rsquo;s notes, so the only person who could know the secret of the relic he was searching for would be the Selective who was taken. He was probably pulled through the portal to Mehrunes Dagon&rsquo;s realm: the Deadlands. But without a way to get there, it would be impossible to retrieve the Selective. 
					</p><p>
					After taking awhile to calm down and think, Karag will recall rumors of a Daedra worshipper being arrested in Cheydinhal. If the rumors are right, he&rsquo;s an expert on making deals with Daedra, and my be able to help the party barter for the Selective, or at least get passage to the Deadlands. 
				</p>
