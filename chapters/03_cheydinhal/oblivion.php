				<h1><a id="oblivion_anc" class="anchor"></a>Onto Oblivion</h1>
				<h2>Quest Overview</h2>
				<h5>XP: 1</h5>
				<p>
					In return for freeing him from the Cheydinhal dungeons, <span class="npc">Albrecht Bombidius</span> has decided to assist the party in contacting a daedra from the Deadlands, so they may retrieve Karag&rsquo;s notes from Oblivion. Upon returning to the Daedric shrine in Kemen, Albrecht begins the ritual to contact the daedra. He is successful in making contact with a dremora named <span class="npc">Lyranth</span>, who agrees to help the party cross into the Deadlands and retrieve the notes, provided they assist her first.
					</p><p>
					Lyranth requires a special item: the <span class="item">Splintered-Light Amulet</span>, which is being held in one of Dagon&rsquo;s strongholds. The party must fight their way up the stronghold to reach its pinnacle, and in the meantime Lyranth will retrieve the notes. Once the party retrieves the amulet, Mehrunes Dagon offers them an alternative deal. In return for killing Lyranth, Dagon will let the party leave with the notes, plus a reward.
				</p>

				<h3><a id="lyranth_anc" class="anchor"></a>Contacting Lyranth</h3>
				<p>
					In order to contact the daedra in Oblivion, Albrecht requests a sigil geode fragment, which he believes can be found in the possession of Cheydinhal&rsquo;s enchanter, Rakul-ko. Rakul-ko will sell the geode to the party for 500 gp, for 200 gp if they have previously done the Party Favors quest, or give it to them in exchange for doing the Party Favors quest.
					</p><p>
					Once the party has the sigil geode, they can meet up with Albrecht in the ruins of Kemen, which have been cleared out since the party&rsquo;s defeat of the Dagon Cult. With the sigil geode, a ritual circle, and a couple corpses of the Dagon Cultists, Albrecht begins the ritual to contact a dremora he has dealt with before. 
					</p><p>
					&ldquo;I call upon you, ancient spirit of Aurbis. I summon you from the depths of Oblivion, from the fires of the Deadlands. I plea for you to come to me. I offer you these bodies, the remains of those who would worship Dagon, to show the sincerity of our desire to bargain with you for passage to your domain,&rdquo; he says to call forth the dremora. Suddenly, behind the party, a female dremora speaks, &ldquo;Again the mortal summons me. Ah. I see this time it has friends. What does it want?&rdquo; She speaks with the party, and if asked may even tell her name, Lyranth. She always speaks to the party in the third person, referring to people as &ldquo;it&rdquo; and &ldquo;mortal&rdquo; but never by a name or other pronoun. 
					</p><p>
					Lyranth&rsquo;s tribe is currently indentured under Dagon. However they wish to leave Dagon&rsquo;s service and turn to another domain. In order to entice a new patron, they are seeking to steal several artifacts from Dagon. One such item is the <span class="item">Splintered-Light Amulet</span>, which was a device made by the Ayleids to gather the magicka in starlight. She knows the location of the amulet and the Marukhati Selective Dagon has captured. If the party recovers the amulet from the top of the tower holding it, she will give them the selective, as well as the <span class="item">Glass Daedra Heart</span>, an enchanted piece of sigil geode which can act as replacement for daedra hearts. 
				</p>

